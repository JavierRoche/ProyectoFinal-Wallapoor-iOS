import UIKit

extension UIFont {
    class var fontStyle22Bold: UIFont {
        return UIFont.systemFont(ofSize: 22.0, weight: .medium)
    }
    
    class var fontStyle18Bold: UIFont {
        return UIFont.systemFont(ofSize: 18.0, weight: .bold)
    }
}
